.MODEL SMALL
.STACK 100H
.DATA
ARR1 DB 50 DUP(30H)
CGPA DB '$'
CNT DB ?
COL DB ?
.CODE

MAIN PROC FAR
MOV AX,@data
MOV DS,AX
MOV ES,AX

MOV AH,05H
MOV AL,00
INT 10H

MOV AH,0FH
INT 10H
PUSH AX

CALL SCRN
CALL INPUTLE
CALL MIDPECUR

MOV AH,09H
LEA DX,ARR1
INT 21H
CALL KEYRES

POP AX
MOV AH,00H
INT 10H
MOV AX,4C00H
INT 21H
MAIN ENDP

INPUTLE PROC NEAR

LEA SI,ARR1
MOV CNT,00D

XXX1: 
MOV AH,10H
INT 16H
CMP AH,01H
JE BBB1
MOV [SI],AL
INC SI
INC CNT
JMP XXX1
BBB1:
MOV AL,CGPA
MOV [SI],AL
RET

INPUTLE ENDP

SCRN PROC NEAR
MOV AH,06H
MOV AL,00H
MOV BH,07
MOV CH,00
MOV CL,00
MOV DH,24
MOV DL,79
INT 10H
RET
SCRN ENDP

MIDPECUR PROC NEAR
SHR CNT,01D
MOV COL,39D
MOV AL,CNT
SUB COL,AL

MOV AH,02H
MOV BH,00
MOV DH,12
MOV DL,COL
INT 10H
RET
MIDPECUR ENDP

KEYRES PROC NEAR
MOV AH,10H
INT 16H
RET
KEYRES ENDP

END MAIN